const char *tr_weather(Lang lang, int symbol) {
    if (symbol < 1 || symbol > 27) return tr(lang, S_WX_UNKNOWN);
    return tr(lang, (StrId)(S_WX_1 + symbol - 1));
}

const char *tr_compass(Lang lang, int degrees) {
    while (degrees < 0)    degrees += 360;
    while (degrees >= 360) degrees -= 360;
    int sector = ((degrees + 22) / 45) % 8;
    return tr(lang, (StrId)(S_CMP_N + sector));
}
